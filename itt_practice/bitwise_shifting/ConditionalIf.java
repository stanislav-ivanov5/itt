package bitwise_shifting;

public class ConditionalIf {

	public  static void main(String[] args){
		
		boolean a = false;
		int b = a ? 3 : 4;
		
		System.out.println(b);
		
		boolean c = true;
		// variable d constitutes whether you have money or not
		String d = c ? "You have money" : "naah" ;
		
		System.out.println(d);
	}
}
